'''Metodo que realiza una suma atravez de los parametros
a y b.
Retorna la suma de los valores'''
def suma(operandoUno,operandoDos):
    if operandoUno !=None and operandoDos!=None:
     return (operandoUno+operandoDos)

def resta(operandoUno,operandoDos):
    if operandoUno !=None and operandoDos!=None:
     return (operandoUno-operandoDos)

def multiplicaion(operandoUno,operandoDos):
    if operandoUno !=None and operandoDos!=None:
     return (operandoUno*operandoDos)

def division(operandoUno,operandoDos):
    if operandoUno !=None and operandoDos!=None and operandoDos !=0:
     return (operandoUno/operandoDos)


